#!/bin/bash

# Папка для сохранения изображений
DEST_DIR="public/images"

# Проверка существования директории
if [ ! -d "$DEST_DIR" ]; then
  echo "Директория $DEST_DIR не существует. Создаю..."
  mkdir -p "$DEST_DIR"
fi

# Функция для загрузки изображения
download_image() {
  local url=$1
  local filename=$2
  echo "Загружаю $filename..."
  curl -o "$DEST_DIR/$filename" "$url"
  if [ $? -eq 0 ]; then
    echo "Успешно загружено: $filename"
  else
    echo "Ошибка при загрузке $filename"
  fi
}

